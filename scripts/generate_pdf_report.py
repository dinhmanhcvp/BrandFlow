def generate_report(json_path, output_path):
    data = load_data(json_path)
    styles = get_styles()

    doc = SimpleDocTemplate(
        output_path, pagesize=A4,
        leftMargin=50, rightMargin=50,
        topMargin=60, bottomMargin=50,
        title=f"{data['company']['name']} Marketing Plan",
        author=", ".join([a["name"] for a in data["authors"]])
    )

    elements = []
    elements += build_cover_page(data, styles)
    elements += build_toc(styles)
    elements += build_business_summary(data, styles)
    elements += build_swot(data, styles)
    elements += build_initiatives(data, styles)
    elements += build_target_market(data, styles)
    elements += build_market_strategy(data, styles)
    elements += build_budget(data, styles)
    elements += build_channels(data, styles)
    elements += build_technology(data, styles)

    doc.build(elements, onFirstPage=header_footer, onLaterPages=header_footer)
    print(f"[OK] Report generated: {output_path}")
    return output_path


if __name__ == "__main__":
    script_dir = os.path.dirname(os.path.abspath(__file__))
    base_dir = os.path.dirname(script_dir)
    json_file = sys.argv[1] if len(sys.argv) > 1 else os.path.join(base_dir, "data", "sample_marketing_plan.json")
    output_file = sys.argv[2] if len(sys.argv) > 2 else os.path.join(base_dir, "output", "Marketing_Plan_Report.pdf")
    os.makedirs(os.path.dirname(output_file), exist_ok=True)
    generate_report(json_file, output_file)
